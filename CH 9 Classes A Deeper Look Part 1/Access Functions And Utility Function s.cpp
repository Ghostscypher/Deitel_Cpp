
/*Access function are function that can read and display data.
Another common use for access functions is the the truth value of
conditions, such functions are often refered to as predicate functions.
Example of predicate function are isEmpty, isFull, isNumeric etc.

Utility class also called helper function is not part of the class
public interface. Rather it's private member function that supports
the operation of class other member functions. Utility functions are not
intended to be used by clients of the class but can be used by friends
of the class.*/